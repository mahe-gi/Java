package helloworld;
/* .Pascal casing
	rules : 1.No space in the sentence.
		2.No symbols except _ and $
		3.First letter of each word should be capital.
		4.sentence should start with capital letter.

example : 
English : Dog loves to play.
Pascal  : Dog_Loves_To_Play
Pascal  : DogLovesToPlay

2.camel casing 
	rules : 1.No space in the sentence.
		2.No symbols except _ and $
		3.First letter of each word should be capital.
		4.sentence should start with small letter.

example : 
English : Dog loves to play.
camel : dog_Loves_To_Play
camel : dogLovesToPlay

Note : class name follows Pascal casing.
*/

public class CasingInJAva {
	
	public static void main(String[] args) {
		
//		variables and methods follows camelCase
		
		int var=0;
		
		System.out.println(var);
	}

}
